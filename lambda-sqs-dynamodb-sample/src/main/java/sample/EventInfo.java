package sample;

public class EventInfo {

  private String deviceId;
  private int year;
  private int month;
  private int day;
  private int hour;
  private int temperature;
  private String uom;

  public String getDeviceId() {
    return deviceId;
  }

  public int getYear() {
    return year;
  }

  public int getMonth() {
    return month;
  }

  public int getDay() {
    return day;
  }

  public int getHour() {
    return hour;
  }

  public int getTemperature() {
    return temperature;
  }

  public String getUom() {
    return uom;
  }
}
